class Solution {
public:
    int climbStairs(int n) {
        int dp[3];
        dp[1]=1;
        dp[2]=2;
        if(n<=2) return n;
        for(int i=3;i<=n;i++){
            int sum=dp[1]+dp[2];
            dp[1]=dp[2];
            dp[2]=sum;
        }
        return dp[2];
    }
};